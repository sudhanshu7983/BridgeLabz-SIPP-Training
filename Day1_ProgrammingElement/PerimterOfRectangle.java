public class PerimterOfRectangle {
    public static void main(String[] args) {
        double length = 5.0;
        double width = 3.0;
        double perimeter = 2 * (length + width);
        System.out.println("The perimeter of the rectangle with length " + length + " and width " + width + " is " + perimeter);
    }
    
}
